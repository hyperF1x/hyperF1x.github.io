var arr = str.split('\n');
var count = 0;

function calc(){
    let document_counter = document.getElementById('counter');
    let document_input = document.getElementById('num_input');
    let document_result = document.getElementById('result');    

    const input = document_input.value;
    // 입력란 내용 지우기
    document_input.value = '';

    if(input < 500 | input > 699){
        //alert('500~699사이의 값을 입력해주세요.');
        document.getElementById("result").innerText = '500~699사이의 값을 입력해주세요.';
        return;
    }

    document_counter.innerText = count+'번째 계산';
    count++;

    var num = parseInt(input)-500;
    const result = arr[num];

    document_result.innerText = result;
    navigator.clipboard.writeText(result);
}